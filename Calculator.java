/**
 * This class represents a Calculator.
 * It provides basic arithmetic operations.
 *
 * @author Deepak
 * @version 1.0
 */
public class Calculator {

    /**
     * Adds two integers.
     *
     * @param a first number
     * @param b second number
     * @return sum of a and b
     */
    public int add(int a, int b) {
        return a + b;
    }
}